<h2 class="py-3 text-center">¿Quiénes Somos?</h2>
<div class="row px-5">
    <div class="col col-7">
        <h3>Empresa</h3>
        <p class="font-sec">
            Arandu surge como empresa familiar en el año 1987, de la necesidad que se veía en la capital federal de la
            presencia de una talabartería que acerque a la ciudad los mejores productos ligados a la actividad del campo
            y sus costumbres. Este producto, que no es sólo producto, sino sabiduría, historia, costumbre; y que se
            caracteriza por estar arraigado a las raíces de nuestra cultura, que ya cuenta con más de dos siglos de
            propia historia. <br>
            Siendo una marca que realza el producto nacional, nos vemos obligados, con orgullo, a transmitir a las
            generaciones venideras eso que, como país, nos ha hecho grandes. Con materiales 100% nacionales, como cuero
            de búfalo, carpincho y vacuno, las más finas lanas de oveja y llama, el metal blanco, la plata, la alpaca y
            el oro, piedras, maderas, entre otros.
        </p>
    </div>
    <div class="col col-5">
        <h3>Misión</h3>
        <p class="font-sec">
            Mantener y promover nuestros usos y costumbres tradicionales, que junto al arte y el campo, forman parte del
            patrimonio cultural que nos identifica.
        </p>
        <h3>Visión</h3>
        <p class="font-sec">
            Afianzar nuestra marca en el tiempo, para que Arandú sea reconocida como la mayor talabartería del país,
            como así también el mayor referente cultural de la Argentina rural.
        </p>
    </div>
    <div class="col col-12">
        <h3>Calidad</h3>
        <p class="font-sec">
            La calidad con la que trabajamos es, de principio a fin, un compromiso que tenemos con lo que hacemos. Desde
            la selección de las materias primas hasta la confección final de todos los productos que ofrecemos, usted
            podrá ver que hay en ellos los más altos niveles de calidad y el máximo cuidado en el diseño y terminación.
            Compartimos con nuestros clientes las mismas exigencias, para que nuestros productos garanticen la mayor
            duración en el tiempo y que su uso satisfaga las necesidades para las cuales fue adquirido. El trabajo
            manual es la fuente principal de nuestros productos, siendo la perfección de este el atributo más
            importante. Las manos, de nuestros artesanos, sintetizan esa pasión con la que trabajamos.
        </p>
    </div>
</div>