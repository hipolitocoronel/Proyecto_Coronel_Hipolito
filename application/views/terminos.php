    <div class="datos-pers">
        <p class="font-sec">
            <b>2. ¿Qué datos personales recogemos y cómo se recogen?</b><br>
            Los datos personales son información relativa a una persona física identificada o identificable. Estos
            pueden incluir, por ejemplo, el nombre, la dirección y el sexo de una persona.
            Podemos recoger datos personales directamente de usted (por ejemplo, cuando compra un producto en una
            tienda) o indirectamente (por ejemplo, de sus dispositivos electrónicos cuando estos interactúan con
            nuestros sitios web, formularios electrónicos o aplicaciones móviles («Plataformas digitales»)).
        </p>
    </div>
    <div class="tiempo-datos">
        <p class="font-sec">
            <b>3. ¿Durante cuánto tiempo conservamos sus datos personales?</b><br>
            Sus datos personales se tratan durante el periodo de tiempo necesario con los fines para los que se hayan
            recogido, para cumplir con las obligaciones legales y reglamentarias, y durante el periodo de tiempo
            necesario para establecer, ejercer o defender cualquier derecho legal.
            Para determinar los periodos de retención más adecuados para sus datos personales, hemos considerado
            específicamente la cantidad, naturaleza y sensibilidad de sus datos personales, las razones por las cuales
            los recogemos, el servicio que usted merece y espera de nosotros y los requisitos legales aplicables.
        </p>
    </div>
    <div class="proteger-datos">
        <p class="font-sec">
            <b>4. ¿Cómo protegemos sus datos personales?</b><br>
            Todos sus datos personales son estrictamente confidenciales y solo estarán al alcance, conforme al principio
            de la «necesidad de conocimiento», del personal debidamente autorizado de Arandú y otras entidades del grupo
            Arandú, y proveedores externos que actúen en nuestro nombre y que cuenten con medidas de seguridad técnicas
            y organizativas adecuadas. Arandú ha implementado medidas de seguridad para proteger sus datos personales
            contra el acceso y uso no autorizados. Seguimos procedimientos de seguridad adecuados para la conservación y
            divulgación de sus datos personales con el fin de evitar el acceso no autorizado por parte de terceros y
            evitar que sus datos se pierdan accidentalmente. Restringimos el grupo de quienes acceden a sus datos
            personales a aquellos que tengan una verdadera necesidad comercial de acceder a ellos. También contamos con
            procedimientos implementados para gestionar cualquier sospecha de violación de la seguridad de los datos. Le
            informaremos a usted y a cualquier entidad reguladora pertinente acerca de cualquier presunta violación de
            la seguridad de los datos cuando así lo exija la ley.
            Asimismo, exigimos a las partes a quienes transferimos sus datos personales que sigan los mismos
            procedimientos.
        </p>
    </div>
